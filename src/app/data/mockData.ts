// Mock data for the Event Lottery System

export interface Event {
  id: string;
  name: string;
  description: string;
  organizerId: string;
  organizerName: string;
  startDate: string;
  endDate: string;
  registrationOpen: string;
  registrationClose: string;
  location: string;
  locationCoords?: { lat: number; lng: number };
  price: number;
  capacity: number;
  waitingListLimit?: number;
  geolocationRequired: boolean;
  radiusRestrictionKm?: number; // 1-500km radius restriction
  posterUrl?: string;
  waitingListCount: number;
  selectedCount: number;
  confirmedCount: number;
  tags: string[];
  flagged?: boolean;
  flagReason?: string;
}

export interface Entrant {
  id: string;
  name: string;
  email: string;
  phone?: string;
  deviceId: string;
  notificationsEnabled: boolean;
  geolocationEnabled: boolean;
  currentLocation?: { lat: number; lng: number; address: string };
  joinedEvents: string[];
  selectedEvents: string[];
  confirmedEvents: string[];
  rejectedEvents: string[];
}

export interface WaitingListEntry {
  entrantId: string;
  entrantName: string;
  eventId: string;
  joinedAt: string;
  location?: { lat: number; lng: number; address: string };
  status: 'waiting' | 'selected' | 'confirmed' | 'rejected' | 'cancelled';
}

export interface Notification {
  id: string;
  userId: string;
  eventId: string;
  eventName: string;
  type: 'selected' | 'not-selected' | 'confirmed' | 'cancelled' | 'general';
  message: string;
  timestamp: string;
  read: boolean;
}

